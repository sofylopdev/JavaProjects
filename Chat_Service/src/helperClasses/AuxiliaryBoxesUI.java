    public static void addHorizontalBoxes(Container pane, GridBagConstraints c){
        MyButton button;
        button = new MyButton("Button");
        c.fill = GridBagConstraints.BOTH;
        button.setBorder(null);
        button.setBackground(Colors.lightBlue);
        button.setForeground(Colors.lightBlue);
        c.gridy = 0;
        c.gridwidth = 1;
        c.gridx = 3;
        pane.add(button, c);

        button = new MyButton("Button");
        c.fill = GridBagConstraints.BOTH;
        button.setBorder(null);
        button.setBackground(Colors.lightBlue);
        button.setForeground(Colors.lightBlue);
        c.gridy = 0;
        c.gridwidth = 1;
        c.gridx = 4;
        pane.add(button, c);
    }
    static class MyButton extends JButton {

        private Color hoverBackgroundColor;
        private Color pressedBackgroundColor;

        private MyButton(String text) {
            super(text);
            super.setContentAreaFilled(false);
            hoverBackgroundColor = Colors.lightBlue;
            pressedBackgroundColor = Colors.lightBlue;
        }

        @Override
        protected void paintComponent(Graphics g) {
            if (getModel().isPressed()) {
                g.setColor(pressedBackgroundColor);
            } else if (getModel().isRollover()) {
                g.setColor(hoverBackgroundColor);
            } else {
                g.setColor(getBackground());
            }
            g.fillRect(0, 0, getWidth(), getHeight());
            super.paintComponent(g);
        }

        @Override
        public void setContentAreaFilled(boolean b) {
        }

    }
